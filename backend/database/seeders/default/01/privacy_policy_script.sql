O propósito desta Política é descrever e explicar os princípios e regras gerais de Tratamento de Dados Pessoais que devem nortear as operações da Companhia.

Esta Política deve ser lida juntamente com o Código de Ética e políticas e procedimentos relacionados, sobretudo aqueles relacionados às regras de segurança da informação. Em caso de conflito entre esta Política e outras políticas da Atech, deverá prevalecer a política ou o procedimento mais rigoroso em relação à proteção de Dados Pessoais. Em tais circunstâncias, o Empregado ou os Terceiros deve notificar DPW para que este possa imediatamente tratar do conflito, dar-lhe a recomendação acerca das providências adequadas a serem tomadas e, caso necessário, atualizar a respectiva Política ou procedimento.


Qualquer Empregado flagrado em violação desta Política estará́ sujeito a medidas disciplinares.

Terceiros que forem flagrados em violação desta Política estarão sujeitos à extinção de sua relação comercial sem prejuízo a quaisquer outras medidas reparadoras e jurídicas à disposição da Atech nos termos da lei aplicável e respectivos contratos.

4.4.2 Não Retaliação

A Atech em nenhuma hipótese admitirá a retaliação, represália ou assédio, velado ou de qualquer outra forma, por qualquer Empregado contra qualquer indivíduo, sendo este Empregado ou Terceiro, por qualquer comunicação de boa-fé relacionados a: (i) questões relativas violações reais ou potenciais desta Política; (ii) violação real ou potencial de qualquer lei ou regulamento federal, estadual ou municipal em que esta Política se enquadre. De acordo com essa política, com Código de Ética e Conduta bem como com qualquer política interna relevante, a Atech tomará as medidas cabíveis, contra qualquer Empregado que violar esta declaração de não retaliação.

4.4.3 Treinamento

O treinamento sobre proteção de dados pessoais da Atech ocorrerá de acordo com a programação definida anualmente pelo DPW.


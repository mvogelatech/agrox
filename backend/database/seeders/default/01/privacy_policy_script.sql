4. DESCRIÇÃO E RESPONSABILIDADES
4.1 	A Política

Esta Política deve ser observada pela Atech em todas as operações ao redor do mundo, a conduzir negócios de maneira ética, garantindo o direito à proteção aos Dados Pessoais que são tratados pela Companhia. A presente Política também deve ser observada pelos Terceiros.

Dessa forma, como empresa comprometida com a proteção de Dados Pessoais, a Atech espera que todos os Empregados e Terceiros cumpram esta Política, todos os procedimentos relacionados, o Código de Ética e todas as leis e regulamentações sobre proteção de Dados Pessoais aplicáveis.

Esta Política, e os exemplos nesta fornecidos, não tem como objetivo fornecer respostas a todas as questões e considerações relativas à proteção de Dados Pessoais, mas têm o objetivo de auxiliar o leitor a entender o objeto e a importância da proteção de dados. Portanto, sempre que houver alguma dúvida sobre a aplicação da Política, ou quaisquer dúvidas ou desconfianças em relação à adequação de qualquer conduta, o DPW deverá ser acionado imediatamente, por meio do endereço de e-mail indicado no final desta Política.

Espera-se que todos os Empregados e todos os parceiros de negócios da Atech conheçam esta Política e a observem, reconheçam e reportem possíveis questões relacionadas a situações de proteção de Dados Pessoais com tempo suficiente para que sejam tratadas de maneira adequada pelo DPW.


4.2 	Diretrizes

4.2.1 Princípios

Todas as atividades de Tratamento realizadas por aqueles em que esta Política se aplica deverão respeitar os seguintes princípios:

a)	[LICITUDE, LEALDADE E TRANSPARÊNCIA]: Respeitar a lealdade, licitude e transparência no Tratamento de Dados Pessoais, o que significa que os Dados Pessoais somente deverão ser tratados nas hipóteses permitidas pela legislação aplicável, sempre com a máxima transparência ao Titular e de acordo com os mandamentos éticos que conduzem as atividades da Atech;

b)	[MINIMIZAÇÃO]: Tratar o menor volume de Dados Pessoais possíveis para se atingir as finalidades que justificam sua coleta, mantendo-os pelo menor tempo necessário;

c)	[EXATIDÃO]: Adotar medidas razoáveis (técnicas e organizacionais) para manter os Dados Pessoais sempre corretos e atualizados, inclusive com a possibilidade de solicitação de correção pelo próprio Titular;

d)	[PSEUDOANONIMIZAÇÃO]: Realizar, sempre que possível, técnicas de pseudoanonimização que tornem impossível identificar o Titular sem que haja associação com outros dados ou informações complementares (os quais devem estar armazenados de forma a garantir que não ocorrerá a identificação). Em outras palavras, deve-se sempre buscar que os dados tratados sejam desassociados de informações que permitam a identificação direta do Titular;

e)	[SEGURANÇA DA INFORMAÇÃO]: Adotar todas as medidas de segurança
adequadas para garantir a confidencialidade, a disponibilidade e a integralidade dos Dados Pessoais, observando-se as especificações técnicas estabelecidas pela Atech;
f)	[ACCOUNTABILITY/RESPONSABILIDADE PRÓ-ATIVA]: Implementar medidas que permitam demonstrar o cumprimento desta Política e das demais normas aplicáveis à proteção de Dados Pessoais, por exemplo, documentando por escrito todas as medidas adotadas com o objetivo de assegurar a proteção de dados (desde estudos técnicos de medidas de segurança até avaliações de impacto, quando estas forem necessárias de acordo com a legislação aplicável).

g)	[PRIVACY BY DESIGN]: Todo e qualquer produto, serviço ou atividade exercida pela Atech deverá ter como princípio a privacidade e proteção de dados pessoais desde sua concepção, portanto, o DPW sempre deverá ser envolvido em toda e qualquer concepção de nova atividade/sistema/aplicação/produto que exija tratamento de dados pessoais, bem como na atualização das mesmas.

4.3 	Procedimentos

Serão estabelecidos procedimentos específicos nos casos em que seja necessário o tratamento de dados pessoais feitos pela Atech ou por Terceiros, para tanto, as diretrizes abaixo serão seguidas quando da publicação de tais procedimentos:


4.3.1 Viabilizar o Exercício de Direitos – A Atech deverá garantir que todos os Titulares poderão exercer seus direitos:

a)	Correção de dados pessoais que estiverem sendo tratados pela Atech ou Terceiros para os quais a Atech tenha transferido os dados;
b)	Exclusão (quando não haja dever legal que faça com que a Atech mantenha armazenado o respectivo Dado Pessoal);
c)	Limitação do Tratamento e revogação do consentimento, ou seja, o Titular pode solicitar que seus dados sejam tratados de acordo com certos limites (respeitada a legislação aplicável e as diretrizes do do DPW), bem como voltar atrás em relação a seu consentimento para Tratamento de dados, principalmente quando tal Tratamento seja realizado para finalidades comerciais/de marketing.

4.3.2 Cumprir com as Regras de Segurança da Informação

Todo o Tratamento deve ser realizado de acordo com os padrões técnicos e operacionais de Segurança da Informação indicados pela Atech, de modo a se garantir a confidencialidade, disponibilidade e a integridade dos Dados Pessoais.

4.3.3 Manter Canal de Comunicação Ativo e Acessível ao Titular

Os Titulares poderão apresentar qualquer dúvida ou reclamação de seus direitos de proteção de dados junto à Atech no e-mail dpo@atech.com.br. Os Titulares também poderão reportar violações ou suspeitas de violações ligadas ao tema por meio do Helpline – Canal de Denúncias, utilizando o site www.atechhelpline.com. A Atech deverá apresentar resposta em prazo razoável, respeitando-se as exigências da legislação local.

